export function getTheoremBySlug(slug: string): TheoremEntry | undefined {
  return THEOREMS.find((th) => th.slug === slug)
}

export function getTheoremsByBranch(branchId: string): TheoremEntry[] {
  return THEOREMS.filter((th) => th.branchId === branchId)
}

export function getTheoremsByTopic(topicSlug: string): TheoremEntry[] {
  return THEOREMS.filter((th) => th.topicSlug === topicSlug)
}

export function searchTheorems(query: string): TheoremEntry[] {
  const q = query.toLowerCase()
  return THEOREMS.filter(
    (th) =>
      th.title.toLowerCase().includes(q) ||
      th.statement.toLowerCase().includes(q) ||
      th.discoveredBy.toLowerCase().includes(q) ||
      th.tags.some((tag) => tag.toLowerCase().includes(q))
  )
}
